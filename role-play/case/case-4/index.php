        <div class="cenario">
            <p>Este cliente possui lojas espalhadas em 10 cidades médias com população entre 200mil e 500 mil habitantes, a compra é centralizada e gerenciada pelo Sr Silvio, a maior parte das lojas foram alocadas próximas de centros de saúde , para chegar nesse numero de lojas os proprietários foram adquirindo outras farmácias, mas a integração dos sistemas e processos ainda não aconteceu, por isso dependendo da loja o cenário em relação a nossa linha pode ser sempre muito diferente, tem loja com estoque de mais e de menos da nossa linha, preços em desacordo com o combinado, falta de produtos, estoque fantasma, VB (valor base sem sentido quando considerada a demanda.</p>
